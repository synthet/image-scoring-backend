def get_firebird_conn(create_new=False):
    # For embedded, we point to the DB file. 
    # Ensure fbclient.dll is loaded from the Embedded folder
    
    # Configure driver to use our embedded DLL
    # We might need to register the server/client in firebird-driver config first?
    # For simplicity, we assume standard connection string if DLL is in path or configured.
    # But strictly for embedded:
    
    # dsn = f"localhost:{os.path.abspath(FIREBIRD_DB)}"
    # For Embedded/Local, use just the absolute path
    db_path = os.path.abspath(FIREBIRD_DB)
    
    print(f"DEBUG: DB Path = {db_path}")
    
    # Let's try basic connection. If it fails, we might need the user to set PATH to include Firebird folder.
    user = os.environ.get("ISC_USER", "sysdba")
    password = os.environ.get("ISC_PASSWORD", "masterkey")
    try:
        if create_new:
             # Create new DB
             print("Creating Firebird DB...")
             # page_size is not a valid kwarg for create_database in recent driver versions
             # It defaults to 8192 or 16384 usually.
             from firebird.driver import create_database
             # Pass db_path as positional argument 'dsn' to avoid internal UnboundLocalError in driver
             create_database(db_path, user=user, password=password)
             
             con = connect(database=db_path, user=user, password=password)
             return con
    except Exception as e:
        print(f"Error creating Firebird database: {e}")
        import traceback
        traceback.print_exc()
        # If creation fails, try to connect anyway, maybe it already exists
        pass # Fall through to regular connect

    return connect(database=db_path, user=user, password=password)


