def migrate_data(sqlite_conn, fb_conn):
    print("Migrating Data...")
    
    tables_map = {
        "jobs": ["id", "input_path", "status", "created_at", "completed_at", "log"],
        "folders": ["id", "path", "parent_id", "is_fully_scored", "created_at"],
        "stacks": ["id", "name", "best_image_id", "created_at"],
        "images": ["id", "job_id", "file_path", "file_name", "file_type", "score", 
                  "score_general", "score_technical", "score_aesthetic", 
                  "score_spaq", "score_ava", "score_koniq", "score_paq2piq", "score_liqe",
                  "keywords", "title", "description", "metadata", "thumbnail_path", "scores_json",
                  "model_version", "rating", "label", "image_hash", "folder_id", "stack_id", "created_at"],
        "file_paths": ["id", "image_id", "path", "last_seen"],
        "resolved_paths": ["id", "image_id", "windows_path", "is_verified", "verification_date", "last_checked"]
    }
    
    sqlite_cur = sqlite_conn.cursor()
    fb_cur = fb_conn.cursor()
    
    for table, columns in tables_map.items():
        print(f"Migrating {table}...")
        try:
            # Select all from sqlite
            col_str = ", ".join(columns)
            sqlite_cur.execute(f"SELECT {col_str} FROM {table}")
            rows = sqlite_cur.fetchall()
            
            if not rows:
                continue
                
            # Insert into firebird
            placeholders = ", ".join(["?" for _ in columns])
            insert_sql = f"INSERT INTO {table} ({col_str}) VALUES ({placeholders})"
            
            # Pre-process rows for DateTime conversion
            processed_rows = []
            
            # Identify timestamp columns indices
            ts_indices = []
            for i, col in enumerate(columns):
                if "created_at" in col or "completed_at" in col or "last_seen" in col or "verification" in col or "checked" in col:
                    ts_indices.append(i)
            
            for row in rows:
                new_row = list(row)
                for idx in ts_indices:
                    val = new_row[idx]
                    if val and isinstance(val, str):
                        try:
                            # Parse ISO format (SQLite default)
                            # Handle potential fractional seconds weirdness if any
                            new_row[idx] = datetime.datetime.fromisoformat(val)
                        except ValueError:
                             pass
                processed_rows.append(tuple(new_row))

            fb_cur.executemany(insert_sql, processed_rows)
            fb_conn.commit()
            print(f"Migrated {len(rows)} rows for {table}")
            
        except Exception as e:
            print(f"Error migrating {table}: {e}")
            
    fb_cur.close()

def main():
    if not os.path.exists(DB_FILE):
        print(f"Source DB {DB_FILE} not found!")
        return

    # User must have Firebird installed/unzipped
    # For now, we assume they have.
    
    print("Connecting to SQLite...")
    s_conn = get_sqlite_conn()
    
    print("Creating Firebird DB...")
    f_conn = get_firebird_conn(create_new=False)
    
    try:
        migrate_schema(s_conn, f_conn)
        migrate_data(s_conn, f_conn)
        print("Migration Complete successfully!")
    except Exception as e:
        print(f"Migration Failed: {e}")
        import traceback
        traceback.print_exc()
    finally:
        s_conn.close()
        f_conn.close()

if __name__ == "__main__":
    main()
